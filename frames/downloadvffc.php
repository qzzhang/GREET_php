<h2>What is GREET Fleet?</h2>
<p>The Department of Energy's Clean Cities Program has enlisted the 
expertise of Argonne to assist in measuring the petroleum displacement 
and greenhouse gas (GHG) emissions of medium- and heavy-duty vehicles 
and off-road equipment. Argonne has developed the GREET Fleet Footprint 
Calculator for Clean Cities stakeholders to estimate these values using 
simple spreadsheet inputs.</p>
<p><strong>Based on GREET.</strong> The basis of this calculator is 
Argonne's <strong>G</strong>reenhouse gases, <strong>R</strong>egulated <strong>E</strong>missions,
 and <strong>E</strong>nergy use in <strong>T</strong>ransportation 
(GREET) fuel-cycle model which is used to generate necessary petroleum 
use and GHG emission co-efficients of key fuel production pathways and 
combustion fuel types.</p>
<p><strong>Well-to-Wheels Results.</strong> Fleet managers can quickly 
insert their data to generate petroleum use and GHG emissions on a 
well-to-wheels (WTW) basis. A WTW analysis can be divided into two 
stages: well-to-pump (WTP) and pump-to-wheels (PTW). The WTP stage 
starts with the fuel feedstock recovery, followed by fuel production, 
and ends with the fuel available at the pump, while the PTW stage 
represents the vehicle's operation activities. It is important to 
examine transportation fuels and technologies on a WTW basis in order to
 properly compare alternatives, as activities upstream of vehicle 
operation can use significant amounts of energy and subsequently produce
 a large amount of emissions.</p>