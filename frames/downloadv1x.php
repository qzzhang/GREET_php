<td style=" padding:5px"> 
<div class="date"> Oct 3, 2014                                                      
</div><b>GREET 1 2014</b>
<p>
The GREET team at Argonne National Laboratory is pleased to announce a new release of the GREET fuel cycle model. This release includes the following updates:
</p>
<ul>
<li>Updated refining efficiency and greenhouse gas (GHG) emission intensity of petroleum products.</li>
<li>Expanded oil sands modeling with more detailed and refined operation data. </li>
<li>Added water consumptions as an additional life-cycle analysis metric. </li>
<li>Added black carbon and organic carbon emissions as an additional criteria air pollutants (CAP) and GHG species. </li>
<li>Added contribution of oil and gas infrastructure to greenhouse gas emissions for crude and natural gas production and processing.  </li>
<li>Added rail module as an additional transportation application. </li>
<li>Added heavy duty vehicle (HDV) module as an additional transportation application. </li>
<li>Updated stationary combustion emission factors. </li>
<li>Added conventional and bio product pathways. </li>
<li>Added pretreatment pathways including dilute acid pretreatment and ammonia fiber expansion.</li>
<li>Added catalyst production pathways. </li>
<li>Added a corn ethanol production process with corn oil extraction and integrated corn/corn stover ethanol production processes. </li>
<li>Updated methane emission for natural gas pathways as well as petroleum venting, fugitive and flaring emissions. </li>
<li>Updated global warming potential (GWP) and added global temperature potential (GTP) based on IPCC 2013 AR5 report.</li>
<li>Updated soybean and biodiesel production assumptions.</li>
<li>Updated cellulosic ethanol production assumptions based on SOT 2013. </li>
<li>Updated CCLUB with two new feedstocks (poplar and willow), new organic carbon emission factors for soil depth of 100 cm, and new land-use change results.</li>
<li>Added ethanol-to-jet pathways. </li>
<li>Updated enzyme and yeast assumptions. </li>
<li>Added Gas/Biomass-To-Liquid (GBTL) pathways and updated Gas-To-Liquid (GTL) pathways.</li>
<li>Added a summary of the 3-season results for the algae hydrothermal liquefaction pathway.</li>
<li>Updated the CO2 emission factor from limestone application. </li>
</ul>
