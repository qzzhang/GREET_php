<b>The model:</b>
<div class="date"> September 20, 2011                                                      
</div>
<p>
This spreadsheet-based tool has been developed to characterize water use in electricity generation from non renewable and renewable sources for the 50 states in the United States.
The tool is built upon a data inventory that analyzes water requirements by fuel source, generation technology, and cooling system. A total of 13 fuels sources and their 19 subcategories and eight electricity generation technologies are included in the inventory.
</p>
