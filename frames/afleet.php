</div><b>AFLEET Tool 2016</b>
<p>
We are pleased to announce a new release of the AFLEET Tool. This release includes the following updates:
<ul>
<li>Added gaseous hydrogen fuel cell vehicles</li>
<li>Added state-based private and public station fuel pricing from Clean Cities Alternative Fuel Price Report</li>
<li>Added refueling station and EVSE infrastructure construction, operation, and maintenance costs</li>
<li>Updated petroleum use, GHG emissions, and relative air pollutant emissions from Argonne’s GREET 1 2015</li>
<li>Updated vehicle air pollutant emission factors from EPA’s MOVES 2014a</li>
<li>Added national petroleum use and GHG emissions externality costs and county-specific air pollutant emission externality costs</li>
<li>For more information on AFLEET updates, please use the following link: <a href="files/afleet-revision-history">AFLEET Tool Version History-2016.pdf</a></li>
</ul></p>
