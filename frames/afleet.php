<td style=" padding:5px">
<div class=date> October 28, 2013
</div><b>What is AFLEET Tool?</b>
<p>
The Department of Energy's Clean Cities Program has enlisted the expertise of Argonne develop a tool to examine both the environmental and economic costs and benefits 
of alternative fuel and advanced vehicles. Argonne has developed the <b>A</b>lternative <b>F</b>uel <b>L</b>ife-<b>C</b>ycle <b>E</b>nvironmental and <b>E</b>conomic <b>T</b>ransportation (AFLEET) Tool for Clean 
Cities stakeholders to estimate petroleum use, greenhouse gas emissions, air pollutant emissions, and cost of ownership of light-duty and heavy-duty vehicles using simple spreadsheet inputs.
</p>
<p>
The tool uses data from Argonne's <b>G</b>reenhouse gases, <b>R</b>egulated <b>E</b>missions, and <b>E</b>nergy use in <b>T</b>ransportation (GREET) fuel-cycle model to generate necessary well-to-wheels 
petroleum use and GHG emission co-efficients for key fuel production pathways and vehicle types. In addition, Environmental Protection Agency&#39;s <b>MO</b>tor <b>V</b>ehicle <b>E</b>mission 
Simulator (MOVES) and certification data are used to estimate tailpipe air pollutant emissions. Various sources are used to provide default cost data, including the Clean Cities 
Alternative Fuel Price Report and American Recovery and Reinvestment Act awards.
</p>
