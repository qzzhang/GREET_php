<div class="date"> October 2, 2015                                                   
</div><b>GREET&trade; 2015 Release</b><br>
<p>
The Argonne National Laboratoryâ€™s Systems Assessment Group is pleased to announce the 2015 release of the suite of GREETTM models and associated reports. This release includes the following expansions and updates:
</p>
<p>
<b>I.	Major Expansions and Updates:</b>
<ul>
  <li>Water consumption of fuel production pathways: developed and updated water consumption for hydrogen from various sources, petroleum fuels, biofuels, and hydro-electric power.  </li>
  <li>Shale oil: developed energy and GHG emissions intensities of U.S. shale oil production with operation data from Bakken and Eagle Ford plays. </li>
  <li>Oil sands: updated GHG emission intensities for Canadian oil sands pathways for surface mining and in-situ technologies. </li>
  <li>High-octane fuels: developed high-octane fuel (HOF, with research octane number of 100) pathways with E10, E25, and E40 ethanol blends.</li>
  <li>Bioethanol: introduced land-management practices of cover crops and manure applications for corn-soy systems for soil organic carbon simulations. </li>
  <li>Waste to energy: expanded and updated waste-to-energy pathways to include renewable natural gas and hydrothermal liquefaction fuels from wastewater treatment plant biosolids and municipal solid waste.</li>
  <li>Aluminum for vehicle production: updated the life-cycle inventory for aluminum. </li>
  <li>Other metals for vehicle production and catalyst production (the latter is for fuel conversion processes): added or updated several metals, including molybdenum, platinum, zinc, nickel and silicon, glass and glass fiber, and Chilean copper.</li>
</ul>
</p>
<p>
<b>II.	 Other Updates:</b>
<ul>
  <li>Natural gas: updated methane leakage emissions for natural gas pathways. </li>
  <li>Electricity generation mix: updated electricity generation mix based on EIA AEO 2015. </li>
  <li>Boiler emission factors: updated stationary combustion emission factors for coal and biomass boilers for industrial applications. </li>
  <li>Bioproducts: updated and expanded the bioproduct module of GREET to include L-lactic acid and ethyl lactate.</li>
  <li>Catalyst for thermochemical biofuels production: developed the energy and material flows for the production of gamma aluminum-based catalysts, including spent catalyst treatment and recovery. </li>
  <li>Soybeans based fuels: updated N2O emissions in soybean fields.</li>
  <li>Vehicle fuel economy and emissions: updated fuel economies and emission factors for both light-duty vehicles (LDVs) and heavy duty vehicles (HDVs). </li>
  <li>Li-ion batteries: expanded and updated material data for Li-ion battery anodes, including graphite and metallic lithium </li>
  <li>Lightweight pickup trucks: updated the material composition of baseline and lightweight pickup trucks. </li>
  <li>Vehicle powertrain material composition: updated the powertrain material composition of several different types of passenger vehicle technologies, including plug-in and fuel cell vehicles, as well as those of spark ignition and compression ignition engine vehicles. </li>
</ul>
</p>
