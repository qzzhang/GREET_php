<p>
<p>For a given vehicle and fuel system, GREET separately calculates the following:
</p>
<ul>
<li>Consumption of total resources (energy in non-renewable and renewable sources), fossil fuels (petroleum, natural gas, and coal together), petroleum, coal, natural gas and water.
</li>
<li>Emissions of CO2-equivalent greenhouse gases - primarily carbon dioxide (CO2), methane (CH4), and nitrous oxide (N2O).
</li>
<li>Emissions of seven criteria pollutants: volatile organic compounds (VOCs), carbon monoxide (CO), nitrogen oxide (NOx), particulate matter with size smaller than 10 micron (PM10), particulate matter with size smaller than 2.5 micron (PM2.5), black carbon (BC), and sulfur oxides (SOx).
</li>
</ul>	</td>
</tr>
